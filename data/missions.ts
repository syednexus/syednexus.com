export function getMissionBySlug(slug: string): Mission | undefined {
  return missions.find((mission) => mission.slug === slug);
}

export function getMissionsByTypes(types: Mission["type"][]): Mission[] {
  const allowed = new Set(types);
  return missions.filter((mission) => allowed.has(mission.type));
}
